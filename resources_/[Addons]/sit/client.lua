function DetectAndSitCustom()

	local ped = GetPlayerPed(-1)

	local playerPos = GetEntityCoords(ped)



	local distanceFromChair = 100.0

	for index,value in ipairs(chairsPos) do

		distanceFromChair = Vdist(playerPos.x, playerPos.y, playerPos.z, value.x, value.y, value.z)

		if(distanceFromChair <= 1.2) then

			DoSitCustom(value)

			return true

		end

	end



	return false

end



function DetectAndSit()

	local ped = GetPlayerPed(-1)

	local playerPos = GetEntityCoords(ped)



	for index,value in ipairs(sitableNames) do

		local obj = GetClosestObjectOfType(playerPos.x, playerPos.y, playerPos.z, 1.0, GetHashKey(value), false, false, false)

		if obj ~= 0 then

			DoSit(obj, value)

			return true

		end

	end

	

	return false

end





local isSitting = false

function DoSit(object, modelName)

	print(modelName)

	local objectPos = GetEntityCoords(object)

    local ped = GetPlayerPed(-1)

	if isSitting then

		ClearPedTasks(ped)

		isSitting = false

	else		

		isSitting = true



		local heading = GetEntityHeading(object)



		local CoordFrom = GetEntityCoords(ped, true)

		local CoordTo = GetOffsetFromEntityInWorldCoords(object, sitables[modelName].xOffset, sitables[modelName].yOffset, 0.0)



		if modelName == "v_med_bed2" or modelName == "v_med_bed1" or modelName == "v_med_emptybed" then

			LoadAnimSet('missfbi1')



			SetEntityCoords(ped, CoordTo)

			SetEntityHeading(ped, (heading+180))



			TaskPlayAnim(ped, 'missfbi1', 'cpr_pumpchest_idle', 8.0, -8.0, -1, 1, 0, false, false, false)

		else

			FreezeEntityPosition(object, true)



			TaskStartScenarioAtPosition(ped, sitables[modelName].scenario, CoordTo.x, CoordTo.y, CoordTo.z - sitables[modelName].verticalOffset, heading + 180.0, 0, true, true)

		end

	end

end



function DoSitCustom(objectCoords)

local ped = GetPlayerPed(-1)

	if isSitting then

		ClearPedTasks(ped)

		isSitting = false

	else		

		isSitting = true



		TaskStartScenarioAtPosition(ped, "PROP_HUMAN_SEAT_BENCH", objectCoords.x, objectCoords.y, objectCoords.z, objectCoords.h, 0, true, true)

	end

end



function LoadAnimSet(AnimDict)

	if not HasAnimDictLoaded(AnimDict) then

		RequestAnimDict(AnimDict)



		while not HasAnimDictLoaded(AnimDict) do

			Citizen.Wait(1)

		end

	end

end



